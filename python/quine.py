def as_data(code):
	s="["
	l=code[0]
	if ord(l)==9:l='\\n'
	elif ord(l)==10:l='\\t'
	elif l=="'":l="\\'"
	elif l=='"':l='\\"'
	elif l=="\\":l='\\\\'
	s=s+"'"+l+"'"
	for l in code[1:]:
		if ord(l)==0x0a:l='\\n'
		elif ord(l)==10:l='\\t'
		elif l=="'":l="\\'"
		elif l=='"':l='\\"'
		elif l=="\\":l='\\\\'
		s=s+",'"+l+"'"
	return s+']'
def as_code(data):
	s=""
	for l in data:
		if l=="\\":l='\\\\'
		s=s+l
	return s
